ll rec(ll l, ll r)
{
    if (l > r)
        return 0;
    if (dp[l][r] != -1)
        return dp[l][r];
    return dp[l][r] = a[r] - a[l] + min(rec(l + 1, r), rec(l, r - 1));
}

